package ru.yarullin.kontur.expression;

public class ExpressionsJsonBody {
    String from;
    String to;

    public ExpressionsJsonBody(String from, String to) {
        this.from = from;
        this.to = to;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
